function buildWorkflowHistory(
  currentStatus: POStatus,
  createdAt: string
): PurchaseOrderWorkflowHistoryEntry[] {
  const currentIndex = workflowSequence.indexOf(currentStatus)

  return workflowSequence.slice(0, currentIndex + 1).map((status, index) => ({
    status,
    changedAt: new Date(
      new Date(createdAt).getTime() + index * 18 * 60 * 60 * 1000
    ).toISOString(),
    changedBy: workflowActors[status],
  }))
}

